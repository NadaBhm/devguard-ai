def build_orchestrator_graph() -> StateGraph:
    """
    Build and compile the LangGraph state graph.
    
    NOTE: Uses MemorySaver for Sprint 1. 
    TODO Sprint 2: Replace with PostgresSaver for persistence across restarts.
    """
    builder = StateGraph(OrchestratorState)

    builder.add_node(
        "codesec_agent",
        lambda s: _safe_node_wrapper(_mock_codesec_agent_impl, "codesec_agent", s)
    )
    builder.add_node(
        "human_gate_1",
        lambda s: _safe_node_wrapper(_human_gate_1_impl, "human_gate_1", s)
    )
    builder.add_node(
        "infracost_agent",
        lambda s: _safe_node_wrapper(_mock_infracost_agent_impl, "infracost_agent", s)
    )
    builder.add_node(
        "human_gate_2",
        lambda s: _safe_node_wrapper(_human_gate_2_impl, "human_gate_2", s)
    )
    builder.add_node(
        "deployops_agent",
        lambda s: _safe_node_wrapper(_mock_deployops_agent_impl, "deployops_agent", s)
    )
    builder.add_node(
        "health_check",
        lambda s: _safe_node_wrapper(_health_check_impl, "health_check", s)
    )
    builder.add_node(
        "generate_report",
        lambda s: _safe_node_wrapper(_generate_report_impl, "generate_report", s)
    )

    builder.set_entry_point("codesec_agent")

    builder.add_conditional_edges(
        "codesec_agent",
        route_after_codesec,
        {"human_gate_1": "human_gate_1", "end": END}
    )
    builder.add_conditional_edges(
        "human_gate_1",
        route_after_gate_1,
        {"infracost_agent": "infracost_agent", "end": END}
    )
    builder.add_conditional_edges(
        "infracost_agent",
        route_after_infracost,
        {"human_gate_2": "human_gate_2", "end": END}
    )
    builder.add_conditional_edges(
        "human_gate_2",
        route_after_gate_2,
        {"deployops_agent": "deployops_agent", "end": END}
    )
    builder.add_conditional_edges(
        "deployops_agent",
        route_after_deployops,
        {"health_check": "health_check", "end": END}
    )
    builder.add_conditional_edges(
        "health_check",
        route_after_health_check,
        {"generate_report": "generate_report", "end": END}
    )
    builder.add_edge("generate_report", END)

    memory = MemorySaver()
    graph = builder.compile(checkpointer=memory)

    logger.info("Orchestrator graph compiled successfully (v1.0.2).")
    return graph


