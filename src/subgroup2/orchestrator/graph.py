def create_initial_state(repo_url: str) -> OrchestratorState:
    """Create the initial state for a new workflow."""
    now = datetime.now(timezone.utc).isoformat()
    job_id = str(uuid.uuid4())

    return {
        "job_id": job_id,
        "repo_url": repo_url,
        "status": "pending",
        "created_at": now,
        "updated_at": now,
        "codesec_result": None,
        "infracost_result": None,
        "deployops_result": None,
        "human_gates": {
            "gate_1_pre_infracost": {
                "required": True,
                "approved": None,
                "comment": None,
                "approved_at": None,
                "approved_by": None,
            },
            "gate_2_pre_deployops": {
                "required": True,
                "approved": None,
                "comment": None,
                "approved_at": None,
                "approved_by": None,
            },
        },
        "error_log": [],
        "orchestrator_metadata": {
            "graph_version": "1.0.2",
            "start_time": now,
            "elapsed_seconds": 0.0,
            "current_node": "start",
            "nodes_executed": [],
            "chat_session_id": None,
        },
        "final_report": None,
    }


def run_workflow(repo_url: str, thread_id: Optional[str] = None) -> OrchestratorState:
    """
    Run the complete orchestrator workflow for a repository.
    
    NOTE: For human gates, execution will pause at interrupt points.
    Resume with: graph.invoke(None, config, resume_after_interrupt=True)
    """
    graph = build_orchestrator_graph()
    state = create_initial_state(repo_url)
    config = {"configurable": {"thread_id": thread_id or state["job_id"]}}

    logger.info(f"Starting workflow for job {state['job_id']} | repo: {repo_url}")

    try:
        final_state = graph.invoke(state, config)
        logger.info(f"Workflow completed for job {state['job_id']} | status: {final_state['status']}")
        return final_state
    except Exception as e:
        logger.error(f"Workflow failed for job {state['job_id']}: {e}")
        state["status"] = "failed"
        state["error_log"].append({
            "node": "orchestrator",
            "attempt": 1,
            "max_attempts": 1,
            "message": str(e),
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "stack_trace": str(e.__traceback__) if e.__traceback__ else None,
            "resolved": False,
        })
        return state


